<template>
  <div id="app">
    <router-view :key="$route.path">
    </router-view>
  </div>
</template>

<script>
export default {
  name: 'App',
  // 设置依赖注入
  provide () {
    return {
      reload: this.reload
    }
  },
  data () {
    return {
      isRouterAlive: true
    }
  },
  methods: {
    reload () {
      this.isRouterAlive = false
      this.$nextTick(() => {
        this.isRouterAlive = true
      })
    }
  }
}
</script>

